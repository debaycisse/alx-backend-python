#!/usr/bin/env python3
"""
This modules houses the definition of a function, named to_str
"""


def to_str(n: float) -> str:
    """
    Converts a given float data to a string equivalent

    Args:
        n - variable that holds the float value to be converted

    Returns:
        the string equivalent of the converted float data
    """
    return "{}".format(n)
