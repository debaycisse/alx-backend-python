#!/usr/bin/env python3
"""
This module houses the definition for a add() function.
"""


def add(a: float, b: float) -> float:
    """
    Sums up two give float type number.

    Args:
        a - variable for the first number
        b - variable for the second number

    Returns:
        the sum value of the two numbers
    """
    return (a + b)
